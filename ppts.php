  <h1>Política de Privacidade - Ticket Sync</h1>
  <p>
    Bem-vindo à <strong>Ticket Sync</strong> (“<strong>nós</strong>”, 
    “<strong>nosso</strong>”, “<strong>a plataforma</strong>”). 
    Esta Política de Privacidade descreve como coletamos, usamos, 
    armazenamos e protegemos as informações pessoais dos usuários
    (“<strong>você</strong>” ou “<strong>usuário</strong>”), de acordo 
    com as boas práticas do mercado, a legislação aplicável e as 
    diretrizes do <strong>Grupo Meta (Facebook/Instagram/WhatsApp)</strong>.
  </p>
  <p>
    Ao utilizar nossos serviços, você confirma que leu e compreendeu 
    esta Política e concorda com a coleta e uso de informações nos termos 
    aqui estabelecidos. Caso não concorde, solicitamos que interrompa 
    o uso da plataforma.
  </p>

  <hr>

  <h2>1. Introdução</h2>
  <p>
    Esta Política de Privacidade tem o objetivo de explicar de forma clara 
    e transparente como tratamos os seus dados pessoais durante a 
    utilização da <strong>Ticket Sync</strong>.
  </p>

  <h2>2. Informações Que Coletamos</h2>

  <h3>2.1. Dados Fornecidos Voluntariamente</h3>
  <ul>
    <li><strong>Informações de Cadastro:</strong> Nome completo, e-mail, 
        telefone/WhatsApp, CPF (quando necessário para emissão de ingressos 
        ou notas fiscais) e outros dados que você opte por compartilhar.</li>
    <li><strong>Dados de Evento/Ingresso:</strong> Informações sobre eventos, 
        ingressos comprados, quantidade, forma de pagamento, entre outros.</li>
  </ul>

  <h3>2.2. Dados Automáticos</h3>
  <ul>
    <li><strong>Logs:</strong> Endereço IP, tipo de dispositivo, versão 
        do navegador, páginas visitadas e datas/horários de acesso.</li>
    <li><strong>Cookies e Tecnologias Similares:</strong> Podemos utilizar 
        cookies para melhorar a experiência do usuário, viabilizar 
        funcionalidades de carrinho de compras e analisar métricas de uso.</li>
  </ul>

  <h2>3. Uso das Informações</h2>
  <p>Utilizamos os dados coletados para:</p>
  <ol>
    <li><strong>Viabilizar a venda de ingressos</strong> e o acesso à plataforma.</li>
    <li><strong>Enviar confirmações de compra</strong>, ingressos digitais 
        e demais comunicações essenciais.</li>
    <li><strong>Atender solicitações de suporte</strong> e resolver problemas 
        técnicos.</li>
    <li><strong>Analisar e melhorar</strong> nosso site, serviços, funcionalidades 
        e segurança.</li>
    <li><strong>Cumprir obrigações legais</strong> e regulatórias, bem como 
        políticas de parceiros (incluindo requisitos do <em>Grupo Meta</em> 
        quanto à exibição de anúncios ou integração com plataformas 
        Facebook/Instagram/WhatsApp).</li>
    <li><strong>Enviar comunicações de marketing</strong> (caso você tenha 
        optado por recebê-las), tais como promoções e novidades sobre eventos. 
        Você pode, a qualquer momento, optar por não recebê-las, seguindo as 
        instruções nos próprios e-mails ou entrando em contato conosco.</li>
  </ol>

  <h2>4. Compartilhamento de Dados com Terceiros</h2>
  <p>Poderemos compartilhar dados pessoais nas seguintes hipóteses:</p>
  <ol>
    <li><strong>Parceiros de Pagamento:</strong> Para processar transações 
        (e.g., Mercado Pago, PIX, instituições financeiras).</li>
    <li><strong>Organizadores de Eventos:</strong> Quando necessário para 
        confirmação de presença ou emissão de ingressos nominalmente.</li>
    <li><strong>Plataformas do Grupo Meta:</strong> Para fins de integração 
        de anúncios, remarketing ou login social, sempre obedecendo às 
        diretrizes de privacidade estabelecidas pela Meta (Facebook/Instagram/WhatsApp).</li>
    <li><strong>Autoridades Legais:</strong> Para atender a exigências 
        judiciais, leis ou regulamentos aplicáveis, ou defender direitos 
        em qualquer processo judicial ou administrativo.</li>
    <li><strong>Fusões e Aquisições:</strong> Em caso de mudança societária, 
        fusão ou venda de ativos, desde que os dados sejam mantidos no mesmo 
        nível de proteção.</li>
  </ol>

  <h2>5. Retenção e Segurança dos Dados</h2>
  <ul>
    <li><strong>Prazo de Retenção:</strong> Conservamos seus dados somente 
        pelo período necessário ao cumprimento das finalidades para as quais 
        foram coletados ou conforme exigido por lei.</li>
    <li><strong>Medidas de Segurança:</strong> Utilizamos protocolos de segurança 
        (como SSL/TLS), controles de acesso e boas práticas de hospedagem para 
        proteger suas informações contra acesso não autorizado, alteração, 
        divulgação ou destruição indevida.</li>
  </ul>

  <h2>6. Direitos do Usuário</h2>
  <p>
    Dependendo da legislação aplicável (LGPD, GDPR etc.), você pode ter os seguintes direitos:
  </p>
  <ul>
    <li><strong>Acesso:</strong> Solicitar confirmação de tratamento e/ou cópia 
        dos dados mantidos sobre você.</li>
    <li><strong>Retificação:</strong> Corrigir ou atualizar dados desatualizados, 
        incompletos ou inverídicos.</li>
    <li><strong>Exclusão:</strong> Solicitar a remoção dos seus dados pessoais 
        (exceto quando houver obrigação legal de mantê-los).</li>
    <li><strong>Oposição:</strong> Opor-se ao uso de dados para determinadas 
        finalidades, como marketing, se aplicável.</li>
    <li><strong>Portabilidade:</strong> Solicitar a transferência de dados para 
        outro fornecedor de serviço ou produto, quando aplicável.</li>
    <li><strong>Revogação de Consentimento:</strong> A qualquer momento, caso o 
        tratamento seja feito com base em consentimento específico.</li>
  </ul>
  <p>
    Para exercer seus direitos, entre em contato conosco por meio dos canais 
    de suporte indicados no final desta Política.
  </p>

  <h2>7. Uso de Serviços do Grupo Meta</h2>
  <ul>
    <li><strong>Login Social:</strong> Caso você opte por fazer login usando 
        conta do Facebook ou Instagram, poderemos receber determinadas 
        informações (nome, e-mail, foto de perfil, etc.) mediante seu consentimento, 
        conforme políticas e permissões do Grupo Meta.</li>
    <li><strong>Anúncios e Remarketing:</strong> Podemos usar Facebook Pixel 
        ou APIs relacionadas para fins de remarketing ou análise de audiência, 
        seguindo as configurações de privacidade e termos do Grupo Meta. Você 
        pode gerenciar suas preferências de anúncios diretamente nas 
        configurações das plataformas do Meta.</li>
  </ul>

  <h2>8. Transferências Internacionais de Dados</h2>
  <p>
    Seus dados poderão ser processados em servidores localizados em outros países, 
    em conformidade com a legislação aplicável e mediante adoção de garantias 
    contratuais, técnicas e organizacionais.
  </p>

